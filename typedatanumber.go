package main

import "fmt"

func main() {
	fmt.Println("int8", int8(20))
	fmt.Println("int16", int16(20000))
	fmt.Println("int32", int32(20000000))
	fmt.Println("int64", int64(2000000000000))
	fmt.Println("uint8", uint8(20))
	fmt.Println("uint16", uint16(20000))
	fmt.Println("uint32", uint32(20000000))
	fmt.Println("uint64", uint64(2000000000000))
}
